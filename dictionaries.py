

# Dictionary: Accepts data in key value pair and can be heterogeneous data types

first_dic = {"a": 2, 4: "Rahul", "c": "Hello"}

print(first_dic[4])
print(first_dic["a"])

# Add values into an empty dictionary
second_dic = {}

second_dic["first_name"] = "Rahul"
second_dic["last_name"] = "Badoni"
print(second_dic)